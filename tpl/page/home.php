<h1>ESL</h1>

<p>Edward Snowden Land ist ein soziales Experiment für direkte Demokratie,
    weil viele Regierungen an unterschiedlichen Stellen versagen.</p>

<p>Wir nehmen die Gesetze unterschiedlicher Länder unter die Lupe, und schlagen Verbesserungen vor.
    Diese werden diskutiert und danach wird abgestimmt.
</p>

<p>Das ESL wird als fiktives Land hinzugefügt, wo wir Gesetze für das Internet aufstellen.</p>

<p>Ein Etappenziel ist ein offener Brief an Frau Sahra Wagenknecht, und eine Petition mit der Forderung nach direkter Demokratie.</p>

<h3>Hintergrund</h3>

<p>In Deutschland sind große Teile der Bevölkerung unzufrieden mit der Politik.
Die derzeitige Koalition scheint festgefahren. Lobbyismus regiert stark mit, und es gibt weitere Mißstände.
Wir wollen die Welt mit diesem Projekt ein Stückchen besser und gerechter machen.
</p>

