<h1>ESL</h1>

<p>Edward Snowden Land is a social experiment for direct democracy,
    because many governments fail on many topics.</p>

<p>We examine the laws of different countries and suggest improvements.
    These are discussed and then voted on.
    If there is a majority, we create a petition for a topic.
</p>

<p>The ESL is added as a fictional country where we make laws for the Internet.</p>

<p>One milestone is a petition calling for direct democracy.</p>

<h3>Background</h3>

<p>In Germany, large parts of the population are dissatisfied with politics.
    The current coalition appears deadlocked. Lobbying has a strong influence, and there are other abuses.
    We want to make the world a little better and fairer with this project.
</p>

